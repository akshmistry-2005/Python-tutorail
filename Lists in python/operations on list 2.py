"""
Operations on list part 2:-
a) extend()
b) remove()
c) pop()
"""
""" A) extend() """
fruits=["mango", "grapes","kiwi"]
print(fruits)
fruits.extend(["banana","watermelon"])
print(fruits)

""" B) remove() """
fruits2=["banana","kiwi","orange","apple"]
print(fruits2)
fruits2.remove("orange")
print(fruits2)

""" C) pop() """
fruit4=["apple","banana","kiwi","orange","grapes","watermelon","pomogrenate","pear"]
print(fruit4)
fruit4.pop(0)
print(fruit4)
