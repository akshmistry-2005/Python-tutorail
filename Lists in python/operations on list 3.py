""" A) REVERSE() """
days_of_week = ["MONDAY","TUESDAY","WEDNESDAY","THURSDAY","FRIDAY","SATURDAY","SUNDAY"]
print(days_of_week)
days_of_week.reverse()
print(days_of_week)

""" B) SORT() """
NUMS= [1,2,4,5,6,7,8,9]
print(NUMS)
NUMS.sort()
print("The Sorted list is: ", NUMS)
NUMS.sort(reverse=True)
print(NUMS)