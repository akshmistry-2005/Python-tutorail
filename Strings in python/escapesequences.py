"""
\n - new line
\t - new tab
\\ - add \
\' - add '
\" - add "
"""
print("aksh \n mistry")
print("aksh \t mistry")
print("aksh \\ mistry")
print ("aksh \' mistry")
print ("aksh \" aksh mistry")


